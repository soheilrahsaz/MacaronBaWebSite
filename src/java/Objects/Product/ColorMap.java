package Objects.Product;

/**
 *
 * @author Moses
 */
public class ColorMap {
    private int id;
    private String color;
    private String taste;

    public int getId() {
        return id;
    }

    public ColorMap setId(int id) {
        this.id = id;
        return this;
    }

    
    
    public String getColor() {
        return color;
    }

    public ColorMap setColor(String color) {
        this.color = color;
        return this;
    }

    public String getTaste() {
        return taste;
    }

    public ColorMap setTaste(String taste) {
        this.taste = taste;
        return this;
    }
    
    
}
