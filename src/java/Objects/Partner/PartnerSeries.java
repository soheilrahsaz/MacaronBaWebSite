package Objects.Partner;

/**
 *
 * @author Moses
 */
public class PartnerSeries {
    private int id;
    private String name;
    private double effect;

    public int getId() {
        return id;
    }

    public PartnerSeries setId(int id) {
        this.id = id;
        
    return this;}

    public String getName() {
        return name;
    }

    public PartnerSeries setName(String name) {
        this.name = name;
    return this;}

    public double getEffect() {
        return effect;
    }

    public PartnerSeries setEffect(double effect) {
        this.effect = effect;
    return this;}
    
    
}
